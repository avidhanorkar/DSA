public class BitwiseOperator{

    public static void main(String[] args) {
        /* Binary And Operator: AND & */
        int a = 5;
        int b = 6;
        int c = a & b;
        System.out.println(c);
        // Binary OR Operator: |
        int d = a | b;
        System.out.println(d);
        // Binary Not Operator: !
        // Binary XOR Operator: ^
        int e = a ^ b;
        System.out.println(e);
        // Binary One's Complement: ~
        int f = ~ a;
        System.out.println(f);
        // Binary Left Shift: <<
        // Binary Right Shift: >>
    }
}
